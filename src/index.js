const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decodeLetter(letter) {
    if(letter.startsWith('*'))
        return ' ';
    letter = letter
        .match(/^0*(.+)/)[1]
        .match(/.{2}/g)
        .map(c => c == '10' ? '.' : '-')
        .join('');
    return MORSE_TABLE[letter];
}

function decode(expr) {
    return expr.match(/.{10}/g).map(decodeLetter).join('');
}

module.exports = {
    decode
}